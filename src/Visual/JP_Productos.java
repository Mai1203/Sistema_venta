    private void Jbn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Jbn_guardarActionPerformed
        if (!Jtf_codigo.getText().isEmpty() && !Jtf_descripcion.getText().isEmpty() && !Jtf_cantidad.getText().isEmpty() && !Jtf_precio.getText().isEmpty() && !Jcb_proveedor.getSelectedItem().toString().isEmpty()) {
            p.setCodigo(Jtf_codigo.getText());
            p.setProducto(Jtf_descripcion.getText());
            p.setPrecio(Double.parseDouble(Jtf_precio.getText()));
            p.setStock(Integer.parseInt(Jtf_cantidad.getText()));
            p.setProveedor(Jcb_proveedor.getSelectedItem().toString());

            produc.RegistrarProducto(p);
            LimpiarJtf();
            LimpiarTable();
            ListarProductos();
            JOptionPane.showMessageDialog(null, "Producto registrado", "Message: Registration", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "No deje campos vacios!", "Error: No data", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_Jbn_guardarActionPerformed

