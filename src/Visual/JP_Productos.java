    private void pdf() throws FileNotFoundException {
        Document documento = new Document();
        try {
            String ruta = System.getProperty("user.home");
            
            File directorio = new File(ruta+"/Documents/facturas");
            if (!directorio.exists()) {
                if (directorio.mkdirs()) {
                    JOptionPane.showMessageDialog(null, "Directorio creado en documentos", "Message: DIR", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Error al crear el directorio", "ERROR: DIR", JOptionPane.ERROR_MESSAGE);
                }
            }

            PdfWriter.getInstance(documento, new FileOutputStream(ruta + "/Documents/facturas/Productos.pdf"));
            documento.open();

            Image img = Image.getInstance("src/Image/icono.png");
            Paragraph fech = new Paragraph();
            Font negrita = new Font(Font.FontFamily.TIMES_ROMAN, 12, Font.BOLD, BaseColor.BLUE);
            fech.add(Chunk.NEWLINE);
            Date date = new Date();
            fech.add("Fecha: " + new SimpleDateFormat("dd-MM-yyyy").format(date) + "\n\n");

            PdfPTable Encabezado = new PdfPTable(4);
            Encabezado.setWidthPercentage(100);
            Encabezado.getDefaultCell().setBorder(0);
            float[] ColumnaEncabezado = new float[]{20f, 30f, 70f, 40f};
            Encabezado.setWidths(ColumnaEncabezado);
            Encabezado.setHorizontalAlignment(Element.ALIGN_LEFT);

            Encabezado.addCell(img);
            Config conf = new Config();
            ProductosDAO proDao = new ProductosDAO();
            conf = proDao.BuscarDatos();

            String ruc = "" + conf.getCc();
            String nom = "" + conf.getNombre();
            String tel = "" + conf.getTelefono();
            String dir = "" + conf.getDireccion();

            PdfPCell cell1 = new PdfPCell(new Phrase(""));
            cell1.setBorder(0);
            Encabezado.addCell(cell1);

            PdfPCell cell2 = new PdfPCell(new Phrase("Ruc: " + ruc + "\nNombre: " + nom + "\nTelefono: " + tel + "\nDireccion: " + dir));
            cell2.setBorder(0);
            Encabezado.addCell(cell2);

            PdfPCell cell3 = new PdfPCell(fech);
            cell3.setBorder(0);
            Encabezado.addCell(cell3);

            PdfPCell cell4 = new PdfPCell(new Phrase(""));
            cell4.setBorder(0);
            Encabezado.addCell(cell4);

            documento.add(Encabezado);

            Paragraph pro = new Paragraph();
            pro.add(Chunk.NEWLINE);
            pro.add("Productos existentes" + "\n\n");
            pro.setAlignment(Element.ALIGN_CENTER);
            documento.add(pro);

            //productos
            PdfPTable tablaPro = new PdfPTable(6);
            Encabezado.setWidthPercentage(100);
            Encabezado.getDefaultCell().setBorder(0);
            float[] ColumnaPro = new float[]{10f, 50f, 15f, 20f};
            Encabezado.setWidths(ColumnaPro);
            Encabezado.setHorizontalAlignment(Element.ALIGN_LEFT);
            PdfPCell pro1 = new PdfPCell(new Phrase("codigo", negrita));
            PdfPCell pro2 = new PdfPCell(new Phrase("Cant.", negrita));
            PdfPCell pro3 = new PdfPCell(new Phrase("Descripción", negrita));
            PdfPCell pro4 = new PdfPCell(new Phrase("Precio U.", negrita));
            PdfPCell pro5 = new PdfPCell(new Phrase("Provee.", negrita));
            PdfPCell pro6 = new PdfPCell(new Phrase("Fecha", negrita));
            pro1.setBorder(0);
            pro2.setBorder(0);
            pro3.setBorder(0);
            pro4.setBorder(0);
            pro5.setBorder(0);
            pro6.setBorder(0);
            pro1.setBackgroundColor(BaseColor.DARK_GRAY);
            pro2.setBackgroundColor(BaseColor.DARK_GRAY);
            pro3.setBackgroundColor(BaseColor.DARK_GRAY);
            pro4.setBackgroundColor(BaseColor.DARK_GRAY);
            pro5.setBackgroundColor(BaseColor.DARK_GRAY);
            pro6.setBackgroundColor(BaseColor.DARK_GRAY);
            tablaPro.addCell(pro1);
            tablaPro.addCell(pro2);
            tablaPro.addCell(pro3);
            tablaPro.addCell(pro4);
            tablaPro.addCell(pro5);
            tablaPro.addCell(pro6);

            List<Productos> listarP = produc.ListarProducto();
            for (int i = 0; i < listarP.size(); i++) {
                String codigo = listarP.get(i).getCodigo();
                String producto = listarP.get(i).getProducto();
                String cantidad = ""+listarP.get(i).getStock();
                String  precio= ""+listarP.get(i).getPrecio();
                String provee = listarP.get(i).getProveedor();
                String fecha = listarP.get(i).getFecha();
                tablaPro.addCell(codigo);
                tablaPro.addCell(cantidad);
                tablaPro.addCell(producto);
                tablaPro.addCell(precio);
                tablaPro.addCell(provee);
                tablaPro.addCell(fecha);
            }

            documento.add(tablaPro);

            Paragraph firma = new Paragraph();
            firma.add(Chunk.NEWLINE);
            firma.add("Ferelinares\n\n");
            firma.add("-----------------------------");
            firma.setAlignment(Element.ALIGN_CENTER);
            documento.add(firma);

            Paragraph mensaje = new Paragraph();
            mensaje.add(Chunk.NEWLINE);
            mensaje.add("Gracias por su Compra.");
            mensaje.setAlignment(Element.ALIGN_CENTER);
            documento.add(mensaje);

            documento.close();
            Desktop.getDesktop().open(new File(ruta + "/Documents/facturas/Productos.pdf"));

        } catch (DocumentException | IOException e) {
            JOptionPane.showMessageDialog(null, e.toString(), "Error: pdf", JOptionPane.ERROR_MESSAGE);
        }
    }
