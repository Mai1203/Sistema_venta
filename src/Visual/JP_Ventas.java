    private void Jbt_abonarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Jbt_abonarActionPerformed
        if (!Jlb_id.getText().equals("")) {
            if (v.getSaldo()>0.0) {
                PagarSaldo pg = new PagarSaldo(v);
                pg.setTablaListener(this);
                pg.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Esta factura ya está pagada");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una venta", "Message: Invoice", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_Jbt_abonarActionPerformed


