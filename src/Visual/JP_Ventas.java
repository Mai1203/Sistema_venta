    public void listarVenta() {
        List<Venta> listarV = vDAO.ListarVentas();
        modelo = (DefaultTableModel) Jtable_ventas.getModel();
        Object[] ob = new Object[8];
        for (int i = 0; i < listarV.size(); i++) {
            ob[0] = listarV.get(i).getId();
            ob[1] = listarV.get(i).getCliente();
            ob[2] = listarV.get(i).getVendedor();
            ob[3] = listarV.get(i).getTipo_pago();
            ob[4] = listarV.get(i).getTotal();
            ob[5] = listarV.get(i).getAbono();
            ob[6] = listarV.get(i).getSaldo();
            ob[7] = listarV.get(i).getFecha();

            modelo.addRow(ob);
        }
        Jtable_ventas.setModel(modelo);
    }

    public void BuscarVentas(String buscar) {
        LimpiarTable();
        List<Venta> listarV = vDAO.BuscarVentas(buscar);
        modelo = (DefaultTableModel) Jtable_ventas.getModel();
        Object[] ob = new Object[8];
        for (int i = 0; i < listarV.size(); i++) {
            ob[0] = listarV.get(i).getId();
            ob[1] = listarV.get(i).getCliente();
            ob[2] = listarV.get(i).getVendedor();
            ob[3] = listarV.get(i).getTipo_pago();
            ob[4] = listarV.get(i).getTotal();
            ob[5] = listarV.get(i).getAbono();
            ob[6] = listarV.get(i).getSaldo();
            ob[7] = listarV.get(i).getFecha();

            modelo.addRow(ob);
        }
        Jtable_ventas.setModel(modelo);
    }

    public void LimpiarTable() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

