    private void Jbt_verVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Jbt_verVentaActionPerformed
        if (!Jlb_id.getText().equals("")) {
            try {
                int id = Integer.parseInt(Jlb_id.getText());
                String ruta = System.getProperty("user.home");
                File file = new File(ruta + "/Documents/facturas/venta_" + id + ".pdf");
                Desktop.getDesktop().open(file);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, ex.toString(), "Error: pdf", JOptionPane.ERROR_MESSAGE);
                java.util.logging.Logger.getLogger(JP_Ventas.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una venta", "Message: Invoice", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_Jbt_verVentaActionPerformed

    private void Jtable_ventasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Jtable_ventasMouseClicked
        int fila = Jtable_ventas.rowAtPoint(evt.getPoint());
        Jlb_id.setText(Jtable_ventas.getValueAt(fila, 0).toString());
        v.setId(Integer.parseInt(Jlb_id.getText()));
        v.setCliente(Jtable_ventas.getValueAt(fila, 1).toString());
        v.setTipo_pago(Jtable_ventas.getValueAt(fila, 3).toString());
        v.setTotal(Double.parseDouble(Jtable_ventas.getValueAt(fila, 4).toString()));
        v.setAbono(Double.parseDouble(Jtable_ventas.getValueAt(fila, 5).toString()));
        v.setSaldo(Double.parseDouble(Jtable_ventas.getValueAt(fila, 6).toString()));
    }//GEN-LAST:event_Jtable_ventasMouseClicked

