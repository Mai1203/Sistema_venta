    private void Jbn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Jbn_actualizarActionPerformed
        if (!Jtf_id.getText().isEmpty()) {
            if (!Jtf_dniruc.getText().isEmpty() || !Jtf_nombre.getText().isEmpty() || !Jtf_direccion.getText().isEmpty() || !Jtf_telefono.getText().isEmpty()) {
                cl.setDni(Integer.parseInt(Jtf_dniruc.getText()));
                cl.setNombre(Jtf_nombre.getText());
                cl.setTelefono(Jtf_telefono.getText());
                cl.setDireccion(Jtf_direccion.getText());
                cl.setId(Integer.parseInt(Jtf_id.getText()));
                cliente.ModificarCliente(cl);
                LimpiarJtf();
                LimpiarTable();
                ListarCliente();
            } else {
                JOptionPane.showMessageDialog(null, "No deje campos vacios!", "Message: No data", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione el cliente", "Message: No data", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_Jbn_actualizarActionPerformed

