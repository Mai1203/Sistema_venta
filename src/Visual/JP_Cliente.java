    private void Jbn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Jbn_guardarActionPerformed
        if (!Jtf_dniruc.getText().isEmpty() || !Jtf_nombre.getText().isEmpty() || !Jtf_direccion.getText().isEmpty() || !Jtf_telefono.getText().isEmpty()) {

            if (Jtf_dniruc.getText().isEmpty()) {
                cl.setDni(0);
            } else {
                cl.setDni(Integer.parseInt(Jtf_dniruc.getText()));
            }
            cl.setNombre(Jtf_nombre.getText());
            cl.setTelefono(Jtf_telefono.getText());
            cl.setDireccion(Jtf_direccion.getText());

            cliente.RegistrarCliente(cl);
            LimpiarJtf();
            LimpiarTable();
            ListarCliente();
            JOptionPane.showMessageDialog(null, "Cliente Registrado", "Message: Customer", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "No deje campos vacios!", "Error: No data", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_Jbn_guardarActionPerformed

