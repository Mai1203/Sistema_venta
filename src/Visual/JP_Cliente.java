    public void BuscarClientes(String buscar){
        LimpiarTable();
        List<Cliente> buscarCl = cliente.Buscadorcliente(buscar);
        modelo = (DefaultTableModel) Jtable_cliente.getModel();
        Object[] ob = new Object[6];
        for (int i = 0; i < buscarCl.size(); i++) {
            ob[0] = buscarCl.get(i).getId();
            ob[1] = buscarCl.get(i).getDni();
            ob[2] = buscarCl.get(i).getNombre();
            ob[3] = buscarCl.get(i).getTelefono();
            ob[4] = buscarCl.get(i).getDireccion();

            modelo.addRow(ob);
        }
        Jtable_cliente.setModel(modelo);
    }
    
    public void LimpiarTable() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

